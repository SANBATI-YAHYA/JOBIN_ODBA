--- ====================================
--- Script de création du rôle ADMIN

ALTER SESSION SET CONTAINER = PDB_FORMATION;


CREATE ROLE ROLE_ADMIN;


  

GRANT 
  CREATE SESSION,
  CREATE USER,
  ALTER USER,
  DROP USER
TO ROLE_ADMIN;

GRANT CREATE TABLE TO ROLE_ADMIN;
GRANT CREATE VIEW TO ROLE_ADMIN;
GRANT CREATE PROCEDURE TO ROLE_ADMIN;
GRANT CREATE SEQUENCE TO ROLE_ADMIN;


GRANT CREATE ANY TABLE TO ROLE_ADMIN;
GRANT DROP ANY TABLE TO ROLE_ADMIN;
GRANT ALTER ANY TABLE TO ROLE_ADMIN;
GRANT SELECT ANY TABLE TO ROLE_ADMIN;

GRANT CREATE ANY SYNONYM TO ROLE_ADMIN;
GRANT DROP ANY SYNONYM TO ROLE_ADMIN;
GRANT INSERT ANY TABLE TO ROLE_ADMIN;
GRANT UPDATE ANY TABLE TO ROLE_ADMIN;
GRANT DELETE ANY TABLE TO ROLE_ADMIN;



--- ====================================
--- Fin du script de création du rôle ADMIN
COMMIT;
